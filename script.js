const ehtemele = document.getElementById("chtml"); // Access the first element

const transform = () => {
    ehtemele.style.width = "200px"; 
    ehtemele.style.height = "200px";
    ehtemele.style.opacity = "100%"
};

const back = () => {
    ehtemele.style.width = "100px"; 
    ehtemele.style.height = "100px";
    ehtemele.style.opacity = "50%"

}
ehtemele.addEventListener("mouseenter", transform);
ehtemele.addEventListener("mouseleave", back);


